<script>
(function() {
	'use strict';

	// --- CSS debug: run on load to see why styles might not load ---
	(function debugCss() {
		console.log('[CSS debug] current URL =', window.location.href);
		var links = document.querySelectorAll('link[rel="stylesheet"]');
		console.log('[CSS debug] Found', links.length, 'stylesheet link(s)');
		links.forEach(function(link, i) {
			var href = link.getAttribute('href');
			var fullUrl = href ? (href.startsWith('http') ? href : new URL(href, window.location.origin).href) : 'no href';
			console.log('[CSS debug]', i + 1, 'href =', href, '-> full URL =', fullUrl);
			fetch(fullUrl, { method: 'HEAD' }).then(function(r) {
				console.log('[CSS debug]', i + 1, fullUrl, '->', r.status, r.statusText, r.ok ? 'OK' : 'FAIL');
			}).catch(function(e) {
				console.log('[CSS debug]', i + 1, fullUrl, '-> FETCH ERROR:', e.message);
			});
		});
	})();

	// Fix hero background image (quote path so spaces/commas work in CSS)
	setTimeout(function() {
		var heroImg = document.querySelector('.grax_tm_hero .bg .image');
		if (heroImg) {
			var path = heroImg.getAttribute('data-img-url');
			if (path) heroImg.style.backgroundImage = "url(" + path + ")";
		}
	}, 100);

	function typeWriter(el, text, speed, onDone) {
		if (!el) return;
		el.textContent = '';
		var i = 0;
		function type() {
			if (i < text.length) {
				el.textContent += text.charAt(i);
				i++;
				setTimeout(type, speed);
			} else if (onDone) onDone();
		}
		type();
	}

	function pillarsCycle() {
		var el = document.getElementById('catch_hero_pillars_text');
		var cursor = document.querySelector('.catch_cursor_pillars');
		if (!el || !cursor) return;
		var words = ['Confidence', 'Visualization', 'Experience'];
		var wordIndex = 0;
		var typeSpeed = 80;
		var eraseSpeed = 50;
		var pauseAfterType = 1800;
		var pauseAfterErase = 400;

		function typeWord(cb) {
			var word = words[wordIndex];
			var i = 0;
			function add() {
				if (i <= word.length) {
					el.textContent = word.slice(0, i);
					i++;
					setTimeout(add, typeSpeed);
				} else setTimeout(cb, pauseAfterType);
			}
			add();
		}

		function eraseWord(cb) {
			var current = el.textContent;
			var len = current.length;
			function remove() {
				if (len > 0) {
					el.textContent = current.slice(0, len - 1);
					len--;
					setTimeout(remove, eraseSpeed);
				} else {
					wordIndex = (wordIndex + 1) % words.length;
					setTimeout(cb, pauseAfterErase);
				}
			}
			remove();
		}

		function loop() {
			typeWord(function() {
				eraseWord(function() {
					loop();
				});
			});
		}
		loop();
	}

	// Start typing sequence after preloader / hero is ready
	function startHeroTyping() {
		var titleEl = document.getElementById('catch_hero_title');
		var subtitleEl = document.getElementById('catch_hero_subtitle');
		var titleCursor = document.querySelector('.catch_hero_name .catch_typing_cursor');
		var subCursor = document.querySelector('.catch_cursor_sub');

		if (!titleEl) return;

		typeWriter(titleEl, 'CATCH MENU', 120, function() {
			if (titleCursor) titleCursor.style.visibility = 'hidden';
			setTimeout(function() {
				typeWriter(subtitleEl, 'A Modern Visual Menu Experience', 70, function() {
					if (subCursor) subCursor.style.visibility = 'hidden';
					setTimeout(pillarsCycle, 400);
				});
			}, 300);
		});
	}

	// Start after load (preloader hides ~1s, then allow typing to start)
	setTimeout(startHeroTyping, 2500);
})();
